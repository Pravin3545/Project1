package star_patten_practice;

public class simple_mirror {
public static void main(String[] args) {
	
	int star=1;
	for (int i = 1; i <=7; i++) {
		for (int j = 1; j <=star; j++) {
			System.out.print("*");
		}
		System.out.println();
		star++;
	}
int	star1=6;
	
	for (int i = 1; i <=6; i++) {
		for (int j = 1; j <=star1; j++) {
			System.out.print("*");
		}
		System.out.println();
		star1--;
	}
	
	
	
	
	
	
	
	
}
}
