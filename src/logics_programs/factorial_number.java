package logics_programs;

public class factorial_number {
public static void main(String[] args) {
	  int fact=1;  
	  //int number=5;//It is the number to calculate factorial    
	  for(int i=1;i<=5;i++){    
	      fact=fact*i;    
	  }    
	  System.out.println("Factorial of "+5+" is: "+fact);    
	 }  
	
}

